const String url = "https://api.met.no/weatherapi/locationforecast/2.0/compact?lat=59.91757&lon=10.82044";

class HttpStream : public Stream
{
private:
    HTTPClient &_client;
    Stream &_stream;

public:
    HttpStream(HTTPClient &client) : _client(client), _stream(client.getStream()) {}

    int available() override
    {
        int available = _stream.available();
        if (available)
        {
            return available;
        }

        while (_client.connected())
        {
            available = _stream.available();
            if (available)
            {
                return available;
            }
        }
        return available;
    }

    int read() override
    {
        int read = _stream.read();
        while (read == -1 && _client.connected())
        {
            read = _stream.read();
        }
        return read;
    }

    int peek() override
    {
        return _stream.peek();
    }

    size_t write(uint8_t data) override
    {
        return _stream.write(data);
    }
};

/**
 * Convert from ISO 8601 time format (e.g. "2023-10-01T12:00:00Z") to local time using the provided timezone.
 */
time_t iso8601toLocalTime(const String &time, tm *timeinfo, Timezone *tz)
{
    timeinfo->tm_year = time.substring(0, 4).toInt() - 1900;
    timeinfo->tm_mon = time.substring(5, 7).toInt() - 1;
    timeinfo->tm_mday = time.substring(8, 10).toInt();
    timeinfo->tm_hour = time.substring(11, 13).toInt();
    timeinfo->tm_min = time.substring(13, 15).toInt();
    timeinfo->tm_sec = time.substring(15, 17).toInt();
    timeinfo->tm_isdst = -1; // auto-detect DST
    time_t t = mktime(timeinfo);
    time_t localTime = tz->toLocal(t);
    localtime_r(&localTime, timeinfo);
    return t;
}

WeatherRange getWeather(std::vector<Day> &days, std::vector<Hour> &hours, Timezone *tz)
{
    NetworkClientSecure client;
    client.setCACert(yrRootCACert);

    HTTPClient http;
    http.useHTTP10(true);
    http.begin(client, url);
    http.GET();
    JsonDocument filter;

    filter["time"] = true;
    filter["data"]["instant"]["details"]["air_temperature"] = true;
    filter["data"]["next_12_hours"]["summary"]["symbol_code"] = true;
    filter["data"]["next_6_hours"]["summary"]["symbol_code"] = true;
    filter["data"]["next_1_hours"]["details"]["precipitation_amount"] = true;
    filter["data"]["next_6_hours"]["details"]["precipitation_amount"] = true;

    JsonDocument doc;

    HttpStream responseStream(http);

    responseStream.find("\"timeseries\":[");

    Day currentDayData;

    time_t firstHour = 0;

    float minTemp = 0;
    float maxTemp = 0;
    float maxPrecipitation = 0;

    do
    {
        DeserializationError error = deserializeJson(doc, responseStream, DeserializationOption::Filter(filter));

        if (error)
        {
            log_d("deserializeJson() failed: %s\n", error.c_str());
            return WeatherRange{minTemp, maxTemp, maxPrecipitation};
        }

        tm timeinfo;
        String time = doc["time"].as<String>();
        time_t t = iso8601toLocalTime(time, &timeinfo, tz);
        if (firstHour == 0)
        {
            firstHour = t - (timeinfo.tm_hour * 3600 + timeinfo.tm_min * 60 + timeinfo.tm_sec);
        }
        JsonObject data = doc["data"].as<JsonObject>();
        String symbol_code = data["next_12_hours"]["summary"]["symbol_code"].as<String>();
        bool is6Hour = data["next_1_hours"].isUnbound();

        JsonObject hoursObj = is6Hour
                                  ? data["next_6_hours"]
                                  : data["next_1_hours"];

        float precipitation_amount = hoursObj["details"]["precipitation_amount"].as<float>();
        String symbol = hoursObj["summary"]["symbol_code"].as<String>();

        float temp = data["instant"]["details"]["air_temperature"].as<float>();
        int hourOffset = (t - firstHour) / 3600;

        log_d("%s: time: %d", time.c_str(), timeinfo.tm_hour);

        if (hourOffset < 24 * 4)
        {
            if (temp < minTemp)
            {
                minTemp = temp;
            }
            if (temp > maxTemp)
            {
                maxTemp = temp;
            }
            float precipitation_per_hour = is6Hour ? precipitation_amount / 6.0 : precipitation_amount;
            if (precipitation_per_hour > maxPrecipitation)
            {
                maxPrecipitation = precipitation_per_hour;
            }
            hours.push_back(Hour{
                hourOffset,
                temp,
                precipitation_per_hour,
                symbol});
        }

        if (timeinfo.tm_mday != currentDayData.date.tm_mday)
        {
            if (currentDayData.date.tm_mday != 0)
            {
                days.push_back(currentDayData);
            }

            currentDayData = Day{timeinfo, 99, -99, symbol_code, precipitation_amount};
        }

        // We want the weather for the day, so we take the symbol code at 06:00.
        // The symbol code may change during the day, but this way we get a consistent forecast for the whole day.
        if (time.substring(11, 13) == "06")
        {
            currentDayData.symbol_code = symbol_code;
        }

        // Find the min and max temperature for the day.
        if (temp < currentDayData.minTemp)
        {
            currentDayData.minTemp = temp;
        }
        if (temp > currentDayData.maxTemp)
        {
            currentDayData.maxTemp = temp;
        }

        currentDayData.precipitation_amount += precipitation_amount;
    } while (responseStream.findUntil(",", "]"));

    // Consume remaining stream data to ensure connection is properly closed
    while (responseStream.available())
    {
        responseStream.read();
    }

    for (const auto &day : days)
    {
        log_d("%04d-%02d-%02d: %f - %f - %s\n", day.date.tm_year + 1900, day.date.tm_mon + 1, day.date.tm_mday, day.minTemp, day.maxTemp, day.symbol_code.c_str());
    }

    http.end();

    return WeatherRange{minTemp, maxTemp, maxPrecipitation};
}

